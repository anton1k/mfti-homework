'''Задача J: Словарь для блондинки
Блондинка Даша любит решать кроссворды на латинском языке, пользуясь орфографическим словарем. Часто Даша отгадывает последние буквы слова и долго ищет каким словам подходит такая концовка.
Она мечтает о словаре, где бы слова были разбиты на главы по количеству букв в слове и написаны задом наперед. Помогите ей составить такой словарь по заданному орфографическому словарю латинском языка.
Формат входных данных
В первой строке содержится единственное целое число N (1≤N≤100) — количество латинских слов в словаре. Далее следует N слов по одному слову на строку. Все слова состоят только из маленьких латинских букв. Общее количество слов на входе не превышает 100. Длина каждого слова не превосходит 15 символов.

Формат результата
Длина слов в данном блоке. На следующих строках слова задом наперед и исходное слово через пробел в лексикографическом порядке.'''

n = int(input())
s = []
for _ in range(n):
    s.append(input())

res = {}
flag = True

for i in s:
    if not res.get(len(i)):
        res[len(i)] = []
    res[len(i)].append(i[::-1])

t = sorted(res)
for i in res:
    res[i] = sorted(res[i])

for i in t:
    for j in res[i]:
        if flag:
            print(i)
            flag = False
        print(j, j[::-1])
    flag = True
        

